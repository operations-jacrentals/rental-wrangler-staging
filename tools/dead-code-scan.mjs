#!/usr/bin/env node
/**
 * dead-code-scan.mjs — a CONSERVATIVE "unreferenced symbol" finder for the
 * frontend, grouped by Code-Atlas chapter. Emits docs/dead-code-report.md.
 *
 * Why static, not a runtime counter: zero performance cost, nothing injected
 * into app.js (so it can't change behavior), and it can't be fooled by an
 * observation window (a counter only proves "didn't run *this week*"). See the
 * spec: docs/superpowers/specs/2026-06-26-code-atlas-reorg-design.md.
 *
 *   node tools/dead-code-scan.mjs        → rewrite docs/dead-code-report.md
 *
 * IMPORTANT — these are CANDIDATES FOR HUMAN REVIEW, never an auto-delete list.
 * This app dispatches a lot of behavior by STRING (e.g. data-act="…" handler
 * names resolved in a switch), and the scan counts string occurrences too, so it
 * is deliberately conservative: a symbol is flagged ONLY when its name appears
 * exactly once in the ENTIRE frontend (its definition). That under-reports dead
 * code on purpose — far better than flagging something live.
 */
import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = join(ROOT, 'docs', 'dead-code-report.md');

const FILES = [
  { rel: 'app.js', key: 'APP' },
  { rel: 'config.js', key: 'CFG' },
  { rel: 'data.js', key: 'DATA' },
  { rel: 'cascade.js', key: 'CASC' },
  { rel: 'service-countdown.js', key: 'SVC' },
  { rel: 'agreements.js', key: 'AGR' },
  { rel: 'icons.js', key: 'ICON' },
];

const RULE = /═{6,}/;
const letterCount = (s) => (s.match(/[A-Za-z]/g) || []).length;
const cleanForLetters = (ln) => ln.replace(/[\/*═]/g, '');
const stampOf = (ln) => (ln.match(/\b([A-Z]+-\d{2,})\s+·/) || [])[1] || null;
const cleanTitle = (ln) => ln
  .replace(/^\s*\/?\*+\s*/, '').replace(/\s*\*+\/?\s*$/, '')
  .replace(/═+/g, '').replace(/^\s*[A-Z]+-\d{2,}\s+·\s+/, '')
  .replace(/\s+/g, ' ').trim().slice(0, 90);

// A module-scope definition: function/const/let at column 0.
const DEF = /^(?:export\s+)?(?:async\s+)?function\s+([A-Za-z0-9_$]+)|^(?:export\s+)?const\s+([A-Za-z0-9_$]+)\s*=|^(?:export\s+)?let\s+([A-Za-z0-9_$]+)\s*=/;

// Build the chapter boundaries for a file (same banner detection as the atlas).
function chaptersOf(lines, key) {
  const singles = [], pures = [];
  for (let i = 0; i < lines.length; i++) {
    if (!RULE.test(lines[i])) continue;
    if (/^\s*\/\*/.test(lines[i]) && letterCount(cleanForLetters(lines[i])) >= 3) singles.push(i);
    else pures.push(i);
  }
  const out = [];
  for (const i of singles) out.push({ start: i, title: cleanTitle(lines[i]), stamped: stampOf(lines[i]) });
  for (let k = 0; k + 1 < pures.length; k += 2) {
    for (let j = pures[k] + 1; j < pures[k + 1]; j++) {
      if (letterCount(cleanForLetters(lines[j])) >= 3) { out.push({ start: pures[k], title: cleanTitle(lines[j]), stamped: stampOf(lines[j]) }); break; }
    }
  }
  out.sort((a, b) => a.start - b.start);
  return out.map((c, n) => ({ ...c, id: c.stamped || `${key}-${String(n + 1).padStart(2, '0')}` }));
}

// Load every file once; build one big corpus for whole-frontend reference counts.
const loaded = FILES.map(({ rel, key }) => {
  const text = readFileSync(join(ROOT, rel), 'utf8');
  return { rel, key, text, lines: text.split('\n') };
});
const CORPUS = loaded.map((f) => f.text).join('\n');

// Count whole-word occurrences of a name across the whole frontend.
function refCount(name) {
  const re = new RegExp(`\\b${name.replace(/[$]/g, '\\$')}\\b`, 'g');
  return (CORPUS.match(re) || []).length;
}

const MIN_LEN = 4;   // skip 1–3 char names (too noisy / too common)

let md = '# Dead-code candidates — frontend (static scan)\n\n';
md += '> Generated by `tools/dead-code-scan.mjs`. **Candidates for human review, '
   + 'NOT an auto-delete list.** A name is listed only if it appears **exactly '
   + 'once** in the entire frontend (its definition). Behavior dispatched by '
   + 'string (e.g. `data-act` handler names) can hide a real use, so verify each '
   + 'before removing. Names shorter than ' + MIN_LEN + ' chars are skipped.\n\n';

let totalDefs = 0, totalDead = 0;
for (const f of loaded) {
  const chapters = chaptersOf(f.lines, f.key);
  const chapterAt = (lineIdx) => {
    let cur = null;
    for (const c of chapters) { if (c.start <= lineIdx) cur = c; else break; }
    return cur;
  };
  // collect defs with their chapter
  const byChapter = new Map();   // id → { title, dead: [{name, line}] }
  for (let i = 0; i < f.lines.length; i++) {
    const m = f.lines[i].match(DEF);
    if (!m) continue;
    const name = m[1] || m[2] || m[3];
    if (!name || name.length < MIN_LEN) continue;
    totalDefs++;
    if (refCount(name) > 1) continue;   // referenced somewhere → not a candidate
    totalDead++;
    const c = chapterAt(i) || { id: f.key, title: '(file top)' };
    if (!byChapter.has(c.id)) byChapter.set(c.id, { title: c.title || f.key, dead: [] });
    byChapter.get(c.id).dead.push({ name, line: i + 1 });
  }
  md += `## ${f.rel}\n\n`;
  if (!byChapter.size) { md += '_No unreferenced top-level symbols found._\n\n'; continue; }
  for (const [id, info] of byChapter) {
    md += `**${id}** — ${info.title}\n\n`;
    for (const d of info.dead) md += `- \`${d.name}\` — ${f.rel}:${d.line}\n`;
    md += '\n';
  }
}

md += `---\n\n_Scanned ${totalDefs} module-scope definitions; ${totalDead} unreferenced candidate(s)._\n\n`;
md += '## Future: real execution coverage (follow-up)\n\n';
md += 'Playwright already drives the app in CI (`smoke`, `logic-test`). A later '
   + 'step can collect JS **coverage** from those runs to show which chapters '
   + 'actually executed — real "active vs. cold" data, with zero production '
   + 'overhead and nothing added to `app.js`. Tracked as its own small spec.\n';

writeFileSync(OUT, md);
console.log(`✓ wrote ${OUT} — ${totalDead} candidate(s) across ${loaded.length} files (of ${totalDefs} defs).`);
