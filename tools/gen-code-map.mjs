#!/usr/bin/env node
/**
 * gen-code-map.mjs — the drift guard for the Code Atlas (docs/CODE-MAP.md).
 *
 * Scans the frontend source files for CHAPTER BANNERS and emits a machine
 * index, docs/code-map.generated.md. The narrated story lives in
 * docs/CODE-MAP.md (hand-written); THIS file owns the volatile facts —
 * chapter list, line ranges, §-anchors, key top-level symbols — so the map
 * can never silently drift out of sync with the code.
 *
 * House pattern: mirrors ci/gen-rule-usage.mjs and ci/check-window-catalog.mjs.
 *   node tools/gen-code-map.mjs           → (re)write docs/code-map.generated.md
 *   node tools/gen-code-map.mjs --check   → exit 1 if the file is out of date
 *
 * Dev-time only. NOT served, NOT imported by the app — it cannot change runtime
 * behavior. No network. Safe to run locally and (optionally) in CI.
 *
 * A "chapter banner" = a block comment that contains a full-width rule line
 * (>= 6 consecutive '=' box-draw chars, U+2550). Per-function /** doc comments
 * have no rule line, so they are never mistaken for chapters.
 */
import { readFileSync, writeFileSync } from 'fs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const OUT = join(ROOT, 'docs', 'code-map.generated.md');

// The frontend files the atlas covers, in narrative (Part I) order.
// key = the short uppercase chapter-ID prefix stamped into banners (Step B).
const FILES = [
  { rel: 'app.js', key: 'APP' },
  { rel: 'config.js', key: 'CFG' },
  { rel: 'data.js', key: 'DATA' },
  { rel: 'cascade.js', key: 'CASC' },
  { rel: 'service-countdown.js', key: 'SVC' },
  { rel: 'agreements.js', key: 'AGR' },
  { rel: 'icons.js', key: 'ICON' },
];

const RULE = /═{6,}/;                 // a full-width ═══ banner rule line
const ANCHOR = /§[0-9]+(?:\.[0-9]+)?[a-z]?/g; // §1, §13.4, §5a, §5.4d …
const SYMBOL = /^(?:export\s+)?(?:async\s+)?function\s+([A-Za-z0-9_$]+)|^const\s+([A-Za-z0-9_$]+)\s*=|^let\s+([A-Za-z0-9_$]+)\s*=/;

/** Clean a candidate title line into a plain chapter title. */
function cleanTitle(ln) {
  return ln
    .replace(/^\s*\/?\*+\s*/, '')   // leading /* or *
    .replace(/\s*\*+\/?\s*$/, '')   // trailing */ or *
    .replace(/═+/g, '')             // box rule chars
    .replace(/^\s*[A-Z]+-\d{2,}\s+·\s+/, '')   // strip the stamped APP-NN · id (Step B); shown in the ID column
    .replace(/\s+/g, ' ')
    .trim()
    .slice(0, 110);
}

/**
 * Find every chapter banner → [{startLine, title, anchors}].
 *
 * Anchored on the ═ rule lines (which appear ONLY in banners — never in code or
 * strings), not on fragile /* … *‍/ tracking. A banner is a cluster of rule
 * lines within 3 lines of each other; its title is the nearest line carrying
 * real letters (the rule line itself for the single-line `/* ═══ TITLE ═══`
 * style, or the line between the top and bottom rules for a full box).
 */
const letterCount = (s) => (s.match(/[A-Za-z]/g) || []).length;

/** The stamped chapter id on a banner title line, if any (e.g. "APP-04"). */
const stampOf = (ln) => (ln.match(/\b([A-Z]+-\d{2,})\s+·/) || [])[1] || null;

function findBanners(lines) {
  const banners = [];
  const pures = [];   // rule lines that carry no title (box delimiters)
  for (let i = 0; i < lines.length; i++) {
    if (!RULE.test(lines[i])) continue;
    const ln = lines[i];
    const opensComment = /^\s*\/\*/.test(ln);
    const t = cleanTitle(ln);
    if (opensComment && letterCount(t) >= 3) {
      // single-line banner:  /* ════ TITLE ════   (title rides the opening rule)
      banners.push({ startLine: i + 1, title: t, anchors: [...(ln.match(ANCHOR) || [])], stamped: stampOf(ln) });
    } else {
      // a box delimiter — an open rule, or a close rule (with or without trailing text)
      pures.push(i);
    }
  }
  // pair pure delimiters as (open, close); title = FIRST letter line between them
  for (let k = 0; k + 1 < pures.length; k += 2) {
    const open = pures[k], close = pures[k + 1];
    let titleIdx = -1;
    for (let j = open + 1; j < close; j++) {
      if (letterCount(cleanTitle(lines[j])) >= 3) { titleIdx = j; break; }
    }
    if (titleIdx >= 0) {
      banners.push({
        startLine: open + 1,
        title: cleanTitle(lines[titleIdx]),
        anchors: [...(lines[titleIdx].match(ANCHOR) || [])],
        stamped: stampOf(lines[titleIdx]),
      });
    }
  }
  return banners.sort((a, b) => a.startLine - b.startLine);
}

/** Top-level symbols (module-scope function/const/let) within [start,end). */
function symbolsIn(lines, start, end) {
  const out = [];
  for (let i = start; i < end && i < lines.length; i++) {
    const m = lines[i].match(SYMBOL);
    if (m) out.push(m[1] || m[2] || m[3]);
  }
  return out;
}

function buildIndex() {
  let md = '<!-- GENERATED by tools/gen-code-map.mjs — DO NOT EDIT BY HAND.\n';
  md += '     The narrated story is docs/CODE-MAP.md; this is its machine index.\n';
  md += '     Regenerate: node tools/gen-code-map.mjs   ·   Drift gate: --check -->\n\n';
  md += '# Code Atlas — generated chapter index (Part I · The Frontend)\n\n';

  let totalChapters = 0;
  const misStamped = [];   // banners whose embedded id != positional id
  for (const { rel, key } of FILES) {
    let text;
    try { text = readFileSync(join(ROOT, rel), 'utf8'); }
    catch { md += `## ${rel}\n\n_(file not found)_\n\n`; continue; }
    const lines = text.split('\n');
    const banners = findBanners(lines);
    md += `## ${rel} — ${lines.length} lines, ${banners.length} chapter${banners.length === 1 ? '' : 's'}\n\n`;
    if (!banners.length) {
      md += `Chapter **${key}** — no internal banners, the whole file is one chapter.\n\n`;
      totalChapters++;
      continue;
    }
    md += '| ID | Lines | Anchors | Chapter title | Key symbols |\n';
    md += '|----|------:|---------|---------------|-------------|\n';
    for (let b = 0; b < banners.length; b++) {
      const id = `${key}-${String(b + 1).padStart(2, '0')}`;
      if (banners[b].stamped && banners[b].stamped !== id) {
        misStamped.push(`${rel}:${banners[b].startLine} stamped ${banners[b].stamped}, expected ${id}`);
      }
      const start = banners[b].startLine;
      const end = b + 1 < banners.length ? banners[b + 1].startLine - 1 : lines.length;
      const syms = symbolsIn(lines, start - 1, end);
      const symStr = syms.length
        ? syms.slice(0, 12).join(', ') + (syms.length > 12 ? `, …(+${syms.length - 12})` : '')
        : '—';
      const anchors = banners[b].anchors.length ? banners[b].anchors.join(' ') : '—';
      const title = banners[b].title.replace(/\|/g, '\\|');
      md += `| ${id} | ${start}–${end} | ${anchors} | ${title} | ${symStr.replace(/\|/g, '\\|')} |\n`;
      totalChapters++;
    }
    md += '\n';
  }
  md += `---\n\n_Total: ${totalChapters} chapters across ${FILES.length} files._\n`;
  if (misStamped.length) {
    console.error('✗ banner id mismatch (a stamped APP-NN no longer matches its file order):');
    for (const m of misStamped) console.error('   ' + m);
    process.exit(1);
  }
  return md;
}

const want = buildIndex();
const check = process.argv.includes('--check');
if (check) {
  let have = '';
  try { have = readFileSync(OUT, 'utf8'); } catch { /* missing → drift */ }
  if (have !== want) {
    console.error('✗ docs/code-map.generated.md is out of date. Run: node tools/gen-code-map.mjs');
    process.exit(1);
  }
  console.log('✓ code map index is current.');
} else {
  writeFileSync(OUT, want);
  console.log(`✓ wrote ${OUT}`);
}
