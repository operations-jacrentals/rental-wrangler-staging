// Generates rule-usage.js: a STATIC catalog of the distinct fields/elements that use
// each design rule, scanned from app.js source (so it covers the WHOLE app, not just
// what's on screen). `--check` compares instead of writing (CI guard vs drift). (Jac)
import { readFile, writeFile } from 'fs/promises';
const src = await readFile(new URL('../app.js', import.meta.url), 'utf8');
// GUARD (SPEC v8 §9.4): no duplicate rule numbers in RULE_META — the R22 closeX/
// dateField collision must never recur. Fails CI on any repeated key.
{
  const block = src.match(/const RULE_META\s*=\s*\{([\s\S]*?)\n\s*\};/);
  if (block) {
    const keys = [...block[1].matchAll(/^\s*(R\d+[a-z]*)\s*:/gm)].map((m) => m[1]);
    const seen = new Set(), dupes = new Set();
    for (const k of keys) { if (seen.has(k)) dupes.add(k); seen.add(k); }
    if (dupes.size) { console.error('✗ DUPLICATE rule numbers in RULE_META: ' + [...dupes].join(', ')); process.exit(1); }
  }
}
const usage = {};
const add = (rule, label) => { if (!label || !label.trim()) return; (usage[rule] = usage[rule] || new Set()).add(label.trim()); };
const clean = (s) => '+' + s.replace(/^\+?\s*(Add\s+)?/i, '');
// addBtn('Label', { opts }) → R5b if link/line/anchor else R5c
for (const m of src.matchAll(/addBtn\(\s*'([^']+)'\s*(?:,\s*\{([^}]*)\})?/g)) add(/\b(link|line|anchor)\b/.test(m[2] || '') ? 'R5b' : 'R5c', clean(m[1]));
for (const m of src.matchAll(/reqBtn\(\s*'([^']+)'/g)) add('R6', m[1]);
for (const m of src.matchAll(/toggleChip\(\s*'([^']+)'/g)) add('R29', m[1]);
for (const m of src.matchAll(/linkName\(\s*'([^']+)'/g)) add('R7', m[1]);
for (const m of src.matchAll(/flagEl\(\s*'([^']+)'(?:\s*,\s*'[^']*'\s*,\s*\{([^}]*)\})?/g)) add(/\balert\b/.test(m[2] || '') ? 'R9b' : 'R9', m[1]);
for (const m of src.matchAll(/dPill\(\s*'([^']+)'(?:\s*,\s*'[^']*'\s*,\s*\{([^}]*)\})?/g)) add(/\balert\b/.test(m[2] || '') ? 'R4b' : 'R4', m[1]);
for (const m of src.matchAll(/actionPill\(\s*'[^']*'\s*,\s*'([^']+)'/g)) add('R17', m[1]);
for (const m of src.matchAll(/ghostPill\(\s*'([^']+)'/g)) add('R18', m[1]);
for (const m of src.matchAll(/closeX\(\s*'([^']*)'/g)) add('R24', m[1] || '✕ (example)');
for (const m of src.matchAll(/dateField\(\s*'([^']*)'/g)) add('R22', m[1] || 'date');
// status pills/gates: the "field" is the status SET they render
for (const m of src.matchAll(/(?:statusPill|gatePill|gatePillRaw)\(\s*'([^']+)'/g)) add('R2', m[1] + ' (status set)');
if (/fileDrop\(/.test(src)) add('R21', 'popup file-drop zones');
// sourceLinkBtn takes a dynamic task.sourceUrl/source, not a literal label — same
// presence-check treatment as fileDrop above.
if (/sourceLinkBtn\(/.test(src)) add('R26', 'service-task manual links (Services · Model schedule)');
const out = {}; for (const k of Object.keys(usage).sort()) out[k] = [...usage[k]].sort();
const content = '/* AUTO-GENERATED by ci/gen-rule-usage.mjs — do not edit by hand. */\nwindow.RULE_USAGE = ' + JSON.stringify(out) + ';\n';
const target = new URL('../rule-usage.js', import.meta.url);
if (process.argv.includes('--check')) {
  const cur = await readFile(target, 'utf8').catch(() => '');
  if (cur !== content) { console.error('✗ rule-usage.js is STALE — run `node ci/gen-rule-usage.mjs`'); process.exit(1); }
  console.log('✓ rule-usage.js is current.');
} else {
  await writeFile(target, content);
  console.log('rule-usage.js written:', Object.fromEntries(Object.entries(out).map(([k, v]) => [k, v.length])));
}
